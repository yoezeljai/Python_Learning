# Script to display strings in different ways

a='hello world!'
print(a)            #Prints complete string
print(a[0])         #Prints 1st character of the string
print(a[2:5])       #Prints characters starting from 3rd to 5th
print(a[2:])        #Prints characters starting from 3rd
print(a*2)          #Prints string in variable a two times
print(a +" TEST")   #prints concatenated string
