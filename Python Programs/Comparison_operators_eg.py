# Script for comparision operators

a=10
b=20
c=0

print("Equal to Equal to Operator")
if(a==b):
    print("a is equal to b")
else:
    print("a is not equal to b")

print("NOT Equal to Operator")
if(a!=b):
    print("a is not equal to b")
else:
    print("a is equal to b")

print("Greater than Operator")
if(a>b):
    print("a is greater than b")
else:
    print("a is not greater than b")

print("Less than Operator")
if(a<b):
    print("a is less than b")
else:
    print("a is not less than b")

print("Greater than Equal to Operator")
if(a>=b):
    print(" a is greater than or equal to b")
else:
    print("a is neither greater than nor equal to b")

print("Less than Equal to Operator")
if(a<=b):
    print("a is less than or equal to b")
else:
    print("a is neither less than nor equal to b")
